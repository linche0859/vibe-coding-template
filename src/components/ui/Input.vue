<script setup lang="ts">
import type { HTMLAttributes } from 'vue'
import { useVModel } from '@vueuse/core'
import { cn } from '@/lib/utils'

const props = defineProps<{
  defaultValue?: string | number
  modelValue?: string | number
  class?: HTMLAttributes['class']
}>()

const emits = defineEmits<{
  'update:modelValue': [value: string | number]
}>()

const modelValue = useVModel(props, 'modelValue', emits, {
  passive: true,
  defaultValue: props.defaultValue,
})
</script>

<template>
  <input
    v-model="modelValue"
    :class="cn(
      'flex h-10 w-full rounded-md border border-neutral-light-600 bg-white px-3 py-2 text-sm ring-offset-background file:border-0 file:bg-transparent file:text-sm file:font-medium file:text-foreground placeholder:text-neutral-light-300 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-primary-heavy-500 focus-visible:ring-offset-2 disabled:cursor-not-allowed disabled:bg-neutral-light-200 disabled:text-neutral-light-300 dark:border-neutral-heavy-100 dark:bg-gray-800 dark:text-white dark:placeholder:text-neutral-heavy-200 dark:disabled:bg-gray-700 dark:disabled:text-neutral-heavy-200',
      props.class,
    )"
  >
</template>
